package person;

public class Person {
	int Age;
	String Name;
	boolean married;
	int child;
	
	
	public int getAge() {
		return Age;
	}
	public void setAge(int age) {
		Age = age;
	}
	
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	
	public boolean isMarried() {
		return married;
	}
	public void setMarried(boolean married) {
		this.married = married;
	}
	
	public int getChild() {
		return child;
	}
	public void setChild(int child) {
		this.child = child;
	}
	
	
}
